package dto;

public class BookMark {
	private Integer num;
	private Integer exnum;
	private String id;
	
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public Integer getExnum() {
		return exnum;
	}
	public void setExnum(Integer exnum) {
		this.exnum = exnum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
